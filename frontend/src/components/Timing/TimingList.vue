<template>
    <div>
        <div class="timing-list">
            <div
                v-if="loading"
                class="loading"
            >
                <Loader />
            </div>
            <div class="timing-list__header">
                <span>Today [{{ todayTimingsCount }}]</span>
            </div>
            <TimingItem
                v-for="item in todayTimings"
                :id="item.id"
                :key="item.id"
                :task-id="item.taskId"
                :start="item.start"
                :end="item.end"
            />
        </div>
        <div class="timing-list">
            <div
                v-if="loading"
                class="loading"
            >
                <Loader />
            </div>
            <div class="timing-list__header">
                <span>Earlier [{{ otherTimingsCount }}]</span>
            </div>
            <TimingItem
                v-for="item in otherTimings"
                :id="item.id"
                :key="item.id"
                :task-id="item.taskId"
                :start="item.start"
                :end="item.end"
            />
        </div>
    </div>
</template>

<script>
    import TimingItem from './TimingItem';
    import Loader from '../Common/Loader';
    import { mapGetters, mapActions } from 'vuex';

    export default {
        name: 'TimingList',
        components: {
            TimingItem,
            Loader,
        },
        data() {
            return {
                loading: true,
            };
        },
        computed: {
            ...mapGetters('timing', [
                'items',
                'todayTimings',
                'otherTimings',
                'todayTimingsCount',
                'otherTimingsCount',
            ]),
        },
        watch: {
            items() {
                if (this.items.length > 0) {
                    this.loading = false;
                }
            },
        },
        mounted() {
            if (this.items.length > 0) {
                this.loading = false;
            }
        },
        methods: {
            ...mapActions('timing', [
                'timingLoad',
            ]),
        },
    };
</script>

